#include "./../../include/calculator.h"
#include <math.h>

double _add(double a, double b) {
	return a+b;
}

double _sub(double a, double b) {
	return a-b;
}

double _mul(double a, double b) {
	return a*b;
}

double _div(double a, double b) {
	return a/b;
}

double _car(double a) {
	return sqrt(a);
}